#include "libft.h"

void	ft_putstr(char *str)
{
	int cpt;

	if (str)
	{
		cpt = 0;
		while (str[cpt] != '\0')
		{
			ft_putchar(str[cpt]);
			cpt = cpt + 1;
		}
	}
}
